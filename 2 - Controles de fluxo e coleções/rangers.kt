for(i in 0..3) {             // 3 é incluido
    print(i)
}
print(" ")

for(i in 0 until 3) {        // 3 não é incluido
    print(i)
}
print(" ")

for(i in 2..8 step 2) {      // Vai pulando de 2 em 2
    print(i)
}
print(" ")

for (i in 3 downTo 0) {      // Vai decrescendo
    print(i)
}
print(" ")
